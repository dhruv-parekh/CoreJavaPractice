package com.practice.classes;

public class Chapter {

	private String name;
	private int page;

	public Chapter(String name, int page) {
		this.name = name;
		this.page = page;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPage() {
		return page;
	}

	public void setPage(int page) {
		this.page = page;
	}

}
